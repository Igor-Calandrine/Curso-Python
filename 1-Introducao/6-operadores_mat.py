""""Temos a representação dos operadores matemáticos abaixo"""

"""Adição"""
print(9 + 13)

"""Subtração"""
print(20 - 5)

"""Multiplicação"""
print(3 * 5)

"""Divisão, retorna um float"""
print(10 / 3)

"""Divisão, retorna número inteiro"""
print(10 // 3)

"""Resto da divisão"""
print(64 % 3)

"""Concatenação, ocorre quando usamos adição em Strings"""
print("Maria" + " " + "Silva")
print("Museu" + " " + "de" + " " + "história")

"""Repetição de String, ocorre quando a multiplicamos"""
print("-" * 10)
print(5 * "Repetição " )

